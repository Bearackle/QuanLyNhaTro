    public Date getSelectedDate(){
        SelectedDate date = dateChooser1.getSelectedDate();
        Date dateClass = null;
        try{
         dateClass = new SimpleDateFormat("dd/MM/yyyy").parse(date.getDay()+"/"+date.getMonth()+"/"+date.getYear());  
        } catch (ParseException ex) { 
            Logger.getLogger(TimePicker.class.getName()).log(Level.SEVERE, null, ex);
        }
        return dateClass;
    }
